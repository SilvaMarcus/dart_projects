// 1.================ Operadores aritméticos:=================
void main() {
  var a = 10;
var b = 5;

// Soma
var soma = a + b; // 15

// Subtração
var subtracao = a - b; // 5

// Multiplicação
var multiplicacao = a * b; // 50

// Divisão
var divisao = a / b; // 2.0

// Resto da divisão (módulo)
var resto = a % b; // 0

// Incremento 
  a++; // a agora é igual a 11

// Decremento 
  b--; // b agora é igual a 4


